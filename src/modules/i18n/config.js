const i18n = new I18n(translations)
i18n.defaultLocale = 'ru'
i18n.enableFallback = true

export function getInitialLocale() {
    const fromStorage = localStorage.getItem(LOCALE_KEY)
    if (fromStorage && LOCALES[fromStorage]) return fromStorage
    return 'ru'
}

export function setI18nLocale(locale) {
    if (!LOCALES[locale]) return
    i18n.locale = locale
    localStorage.setItem(LOCALE_KEY, locale)
}

export function t(key, options) {
    return i18n.t(key, options)
}

export function getLocaleMeta(locale) {
    return LOCALES[locale] || LOCALES.ru
}

export function translateCategory(category, locale) {
    const item = CATEGORY_TRANSLATIONS[category]
    if (!item) return category
    return item[locale] || item.ru || category
}
